    private class TimeCounter extends Thread
    {
        @Override
        public void run() {
            int second = 0;
            while (!isWin) {                
                second++;
                lbTime.setText(second+"");
                try {
                    sleep(1000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
    }
    int size = 3;
    int row,col;
    GridLayout layout;
    boolean isWin;
    
    //Array of JButton
    Component[] arr;
    
    //flag variable indicates whether programe is shuffling
    //if true, checkWin() shouldn't work
    boolean shuffling;
    final String[] ARROWS = {"left", "up", "right", "down"};
    TimeCounter time;
    public NewJFrame() {
        initComponents();
        layout = (GridLayout) panel.getLayout();
        start();                
    }
    private void start()
    {
        panel.removeAll();
        panel.requestFocusInWindow();//to get event key pressed
        //reset position of blank square
        col = size-1; row = size-1;
        
        //set up layout 
        layout.setColumns(size);
        layout.setRows(0);        
        
        //add components        
        for(int i = 1; i < size*size; i++)
        {
            panel.add(new JButton(i+""));
        }
        panel.add(new JButton(" "));
        
        //update UI and shuffle
        arr = panel.getComponents();
        panel.updateUI();       
        shuffle();
        
        //start counting time
        time = new TimeCounter();
        time.start();
    }
    private void shuffle()
    {
        //move the blank square around randomly 10 times
        //its original position is top left (0,0)
        shuffling = true;
        for(int i = 0; i < 20; i++)
        {
            int random = (int) (Math.random()*4);
            move(ARROWS[random]);
        }
        shuffling = false;
    }
    private boolean checkWin()
    {              
        for(int i =0; i < arr.length-1; i++)
        {
            //check if each button text is equal its postion + 1
            JButton btn = (JButton) arr[i];            
            if(!btn.getText().equals( (i+1) + ""))
            {                
                return false;                 
            }
               
        }        
        return true;        
    }
    //move blank square
    private void move(String arrow)
    {
        {
            int pos = row*size + col;
            switch(arrow)
            {
                case "up":
                    if(row > 0)row--;
                    break;
                case "down":
                    if(row < size -1)row++;
                    break;
                case "left":
                    if(col > 0 )col--;
                    break;
                case "right":
                    if(col < size -1) col++;
                    break;
            }
            int nextPos = row*size + col;
            JButton nextBtn = (JButton) arr[nextPos];
            JButton btn = (JButton) arr[pos];
            btn.setText(nextBtn.getText());
            nextBtn.setText(" ");
        }
        isWin = checkWin();
        if(isWin && !shuffling){           
            JOptionPane.showMessageDialog(this, "You won");            
        }
            
    }

